# STEP 3: Train YOLOv8 Detection Model
# ------------------------------
from ultralytics import YOLO
import yaml # Import yaml to write the configuration file

# Define the path for the data.yaml file
yaml_path = "data.yaml"

# Create the data.yaml content
data_yaml_content = {
    'train': 'dataset/train/images',
    'val': 'dataset/valid/images',
    'test': 'dataset/test/images', # Optional
    'nc': 12, # Number of classes based on the dataset you uploaded
    'names': ['back', 'four_way', 'front', 'hand', 'left', 'left_and_front', 'no_entry', 'no_left_turn', 'no_right_turn', 'right', 'straight', 'stop'] # Names of your classes
}

# Write the content to data.yaml
with open(yaml_path, 'w') as file:
    yaml.dump(data_yaml_content, file, default_flow_style=False)

print(f"✅ {yaml_path} created successfully.")

model = YOLO("yolov8n.pt")  # nano detection model

model.train(
    data=yaml_path,
    epochs=50,
    batch=16,
    imgsz=640,
    project="turn_prediction",
    name="yolov8_turn_detector",
    exist_ok=True
)

