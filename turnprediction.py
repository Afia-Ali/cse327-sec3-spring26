best_model = YOLO("runs/detect/turn_prediction/yolov8_turn_detector/weights/best.pt")
class_names = data_yaml_content['names']
print("Class names:", class_names)

# ------------------------------
# STEP 6: Test on 4 Images (Best Confidence)
# ------------------------------
import matplotlib.pyplot as plt
import matplotlib.patches as patches
from PIL import Image
import numpy as np

test_img_dir = "dataset/test/images"

# Pick the 4 images with the highest confidence detections
all_images = os.listdir(test_img_dir)
scored = []
for img_name in all_images:
    test_img_path = os.path.join(test_img_dir, img_name)
    results = best_model.predict(test_img_path, conf=0.25, verbose=False)
    if len(results[0].boxes) > 0:
        top_conf = float(results[0].boxes.conf.max())
        scored.append((top_conf, img_name))

# Sort by confidence and take top 4
scored.sort(reverse=True)
test_images = [img_name for _, img_name in scored[:4]]

fig, axes = plt.subplots(1, 4, figsize=(16, 5))

for ax, img_name in zip(axes, test_images):
    test_img_path = os.path.join(test_img_dir, img_name)
    results       = best_model.predict(test_img_path, conf=0.25, verbose=False)

    img = Image.open(test_img_path)
    ax.imshow(img)

    for box in results[0].boxes:
        x1, y1, x2, y2 = box.xyxy[0].tolist()
        cls   = int(box.cls[0])
        conf  = float(box.conf[0])
        label = f"{class_names[cls]}  {conf*100:.0f}%"

        rect = patches.Rectangle((x1, y1), x2-x1, y2-y1,
                                   linewidth=2, edgecolor='red', facecolor='none')
        ax.add_patch(rect)
        ax.text(x1, y1-5, label, color='red', fontsize=9, fontweight='bold')

    ax.axis("off")
    ax.set_title(img_name[:20], fontsize=8)

plt.suptitle("Detection Results — Top 4 Images", fontsize=14, fontweight='bold')
plt.tight_layout()
plt.show()

